const size_t ICONS_COUNT = sizeof(ICONS) / sizeof(ICONS[0]);

const Icon *find_icon(const char *name) {
  if (name == nullptr || *name == '\0') return nullptr;
  for (size_t i = 0; i < ICONS_COUNT; i++) {
    if (std::strcmp(ICONS[i].name, name) == 0) return &ICONS[i];
  }
  return nullptr;
}

